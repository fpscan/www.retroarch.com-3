            </div>
            <div class="col-sm-6">
                <p>After you have configured netplay as you wish, launch a game and use:</p>
                <ul>
                    <li><b>Start netplay host</b> to act as server</li>
                    <li><b>Connect to netplay host</b> to act as client</li>
                </ul>
